public class Contenedor {

    private Integer numeroContenedor;
    private String paisProcedencia;
    private Boolean materialPeligroso;

    public Contenedor(Integer numeroContenedor, String paisProcedencia, Boolean materialPeligroso) {
        this.numeroContenedor = numeroContenedor;
        this.paisProcedencia = paisProcedencia;
        this.materialPeligroso = materialPeligroso;
    }
}
